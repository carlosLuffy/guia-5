    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed
        // TODO add your handling code here:
        String validar;
        validar = Opciones.getSelectedItem().toString();
        if(validar.equals("Seleccione una opcion")){
            JOptionPane.showMessageDialog(null, "elige una opcion");
        }
        else if (validar.equals("1")){
            ingresar.setVisible(true);
            ingreso.setVisible(true);
            Guardar.setVisible(true);
            ingresar.requestFocus();
            contador1=1;
        }
        if(validar.equals("2")){
            if(contador1==0){
                JOptionPane.showMessageDialog(null, "debe ingresar las letras primero");
            }
            else{
                 Arrays.sort(letras);
                 contador1=1;
                 mostrar_letras.setText("");
            }
        }
         if(validar.equals("3")){
             String salida="";
             if(contador1==0){
                 JOptionPane.showMessageDialog(null, "no ha ingresado letras aun");
             }
             else{
             for(int cont=0;cont<letras.length;cont++){
                 salida=salida+letras[cont]+"\n";
             }
             mostrar.setVisible(true);
             mostrar_letras.setText(salida);
         }
         }
         if(validar.equals("0")){
             System.exit(0);
         }
        
    }//GEN-LAST:event_aceptarActionPerformed

    private void ingresarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ingresarKeyTyped
        // TODO add your handling code here:
        char validar = evt.getKeyChar();
        if((validar<'a' || validar> 'z') && (validar<'A' || validar>'Z')){
            evt.consume();
        }
        if(ingresar.getText().length()>9){
            Guardar.setEnabled(true);
            ingresar.setEnabled(false);
        }
    }//GEN-LAST:event_ingresarKeyTyped

    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        // TODO add your handling code here:
        if (ingresar.getText().equals("")){
            JOptionPane.showMessageDialog(null , "ingres las 10 letras");
            
        }
        else{
            char c;
            cadena=ingresar.getText();
            for(int contador=0;contador<letras.length;contador++){
                c=cadena.charAt(contador);
                letras[contador]=c + "";
            }
        }
            
    }//GEN-LAST:event_GuardarActionPerformed

